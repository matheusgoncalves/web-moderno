/* Exercício 01: Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração, multiplicação e divisão desses valores. */

function operacoes(a, b) {
    console.log(`soma: ${a + b}`);
    console.log(`subtracao: ${a - b}`);
    console.log(`multiplicacao: ${a * b}`);
    console.log(`divisao: ${a / b}`);
}

operacoes(4, 2);